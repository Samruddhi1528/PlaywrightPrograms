//Identifiers, literals, operators

//Identifiers: A name which is given to a container
//Literals: A value which is given to a container
//Operators: A symbol which is used to perform an operation

var a = 10;
console.log("Keyword: var");
console.log("Identifier: a");
console.log("Literal: 10");
console.log("Operator: =");

//Identifiers can start with $ and _
var $name = "Samruddhi"
console.log($name);

var _name = "Samruddhi"
console.log(_name);

//Case sensitivity
var name = "Samruddhi"
var Name = "Samruddhi"
var NAME = "Samruddhi"
console.log(name);
console.log(Name);
console.log(NAME);

//Identifier as a special character
var π = 3.14
console.log(π);

var _ = "Underscore"
console.log(_);

var नमस्ते = "Hi";
console.log(नमस्ते);

