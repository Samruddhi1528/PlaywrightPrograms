// =, ==, ===

console.log(5 == 5); //true
console.log(5 == "5"); //true
console.log(5 === 5); //true
console.log(5 === "5"); //false
console.log(5 != 5); //false
console.log(5 != "5"); //false
console.log(5 !== 5); //false
console.log(5 !== "5"); //true
console.log(5 == 5.0); //true
console.log(5 === 5.0); //true
console.log(5 === 5.01); //false
