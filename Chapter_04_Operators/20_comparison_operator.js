//Comparison operators =, ==, ===

// = -> Assignment opertaor
// == -> Equality operator, loose comparison. Check only value
// === -> Strict equality operator, strict comparison. Check data type and value
// <, >, <=, >=, !, != , !==

console.log(2 == 2)
console.log(2 === 2)
console.log(2 != 2)
console.log(2 !== 2)
console.log(3 > 2)
console.log(3 < 2)
console.log(3 >= 2)
console.log(2 <= 2)

console.log(5 == "5") // loose comparison
console.log(5 === "5") // strict comparison

console.log(5 != "5") // loose comparison
console.log(5 !== "5") // strict comparison
