// -- is pre increement : Increement and then assign
// ++ is post increement : Assign and then increement

//Increament operator
let a = 10;
console.log(a++);
console.log(++a);

//Decreament operator
let b = 10;
console.log(b--);
console.log(--b);

let a1 = 10;
let b1 = ++a1;
console.log(b1);
console.log(a1)


