// 2️⃣ Print numbers from 1 to 10
console.log("Print numbers from 1 to 10")
let j = 1
while (j <= 10) {
    console.log(j)
    j++
}

// 3️⃣ Print even numbers from 1 to 20
console.log("Print even numbers from 1 to 20")
let k = 1
while (k <= 20) {
    if (k % 2 == 0) {
        console.log(k)
    }
    k++
}

// 4️⃣ Calculate sum of first 10 natural numbers
console.log("Calculate sum of first 10 natural numbers")
let l = 1
let sum = 0
while (l <= 10) {
    sum += l
    l++
}
console.log(sum)

